/**
 * mul - function that multiples two integers
 * @a: The number to be multiplied
 *@b: The number to be multiplied
 *@result: The result of the multiplication
 * Return: The result of the multiplication
 */
int mul(int a, int b)
{
    int result; 
    result = a * b;
    return (result);
}